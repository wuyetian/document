一种是innodb,一种是myisam,两者的主要区别是
①myisam不支持事务处理，而innoDB支持事务处理
②myisam 不支持外键，innoDB支持外键
③myisam支持全文检索，而innoDB在MySQL5.6版本之后才支持全文检索
